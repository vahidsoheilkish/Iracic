     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $conference_user = session()->get('conference_user_notice');
        $conference = Conference::where('conference_user_id',$conference_user->id)->first();
        if(isset($conference)) {
            $cities = Cities::where('countries_id', $conference->country)->get();
        }else{
            $cities = [];
        }
        return view('conference.en.panel.new_volume' , compact('conference','cities'));
    }

